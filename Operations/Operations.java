package Operations;

public class Operations {
	public static void main(String[] args) {

		int div = 10 / 3;
		// output: 3
		System.out.println(div);

		float div1 = 10 / 3;
		// output: 3.0
		System.out.println(div1);

		float div2 = 10 / 3.0F;
		// output: 3.3333333
		System.out.println(div2);

		double div3 = 10 / 3.0;
		// output: 3.3333333333333335
		System.out.println(div3);

	}
}
